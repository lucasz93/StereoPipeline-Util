#!/bin/bash

#===============================================================================
#===============================================================================
rootDir=`pwd`/..
installDir=$rootDir/install

#===============================================================================
#===============================================================================
clean_visionworkbench()
{
	rm -rf "$rootDir/visionworkbench/build"
}

#===============================================================================
#===============================================================================
clean_isis3()
{
	rm -rf "$rootDir/ISIS3/build"
}

#===============================================================================
#===============================================================================
clean_stereopipeline()
{
	rm -rf "$rootDir/StereoPipeline/build"
}

#===============================================================================
#===============================================================================
clean_f2c()
{
	rm $rootDir/f2c/src/*.o
	rm "$rootDir/f2c/src/f2c"
}

#===============================================================================
#===============================================================================
clean_cspice()
{
	rm $rootDir/cspice/lib/*.o
	rm $rootDir/cspice/lib/*.so
	rm $rootDir/cspice/lib/*.a
}

#===============================================================================
#===============================================================================
for target in "$@"
	do
	case "$target" in
		"f2c" ) clean_f2c ;;
		"cspice" ) clean cspice ;;
		"vw" ) clean_visionworkbench ;;
		"isis" ) clean_isis3 ;;
		"asp" ) clean_stereopipeline ;;
		"all" )
			clean_f2c
			clean_cspice
			
			clean_visionworkbench
			clean_isis3
			clean_stereopipeline
			
			rm -rf "$rootDir/install"
			;;
		* ) 
			echo "clean: Unknown target '$build_type'"
			echo "./clean.sh [f2c|cspice|vw|isis|asp|all]"
			exit 1
			;;
	esac
done
